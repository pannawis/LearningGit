
# sum
def sum(a,b):

    out = a+b

    return out

# Park multiple
def multiple(a,b):

    out = a*b

    return out

# Benz divided 
def divided(a,b):
    if b == 0:
        out = 0     # If b = 0 we will return 0
    else:
        out = a/b   # Calculate a divided by b
    
    return out      # Return divided result

# sum square
def sum_squre(a,b):
    sum = a + b     # sum 
    out = sum**2    # squre

    return out 

